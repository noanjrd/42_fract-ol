#include "ft_printf.h"

int	u(unsigned int i)
{
	char	*a;
	int		j;

	a = ft_itoa_u(i);
	j = 0;
	while (a[j])
	{
		write(1, &a[j], 1);
		j++;
	}
	free(a);
	return (j);
}
